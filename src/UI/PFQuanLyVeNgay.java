    public void loadTable() {
        ArrayList<TongVeDTO> list = TongVeDAO.tableMave("Vé Ngày");
        Object[] row = new Object[1];
        for (int i = tblModel.getRowCount() - 1; i >= 0; i--) {
            tblModel.removeRow(i);
        }
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getMaVe();
            tblModel.addRow(row);
        }
        TableVeNgay.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (TableVeNgay.getSelectedRow() >= 0) {
                    txtMaCu.setText(TableVeNgay.getValueAt(TableVeNgay.getSelectedRow(), 0) + "");
                    txtXoaVe.setText(TableVeNgay.getValueAt(TableVeNgay.getSelectedRow(), 0) + "");
                }
            }
        });
    }

    public TongVeDTO addVe() {
        TongVeDTO tx = new TongVeDTO();
        tx.setMaVe(txtThemVe.getText());
        tx.setLoaiVe("Vé Ngày");
        tx.setTenKhach(null);
        tx.setSdt(null);
        tx.setBienSo(null);
        tx.setLoaiXe(null);
        tx.setMauXe(null);
        tx.setNgayDk(LocalDate.now());
        tx.setSoTien(0);
        return tx;
    }

